    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        com = (String) jComboBox1.getSelectedItem();
        try {

            myconn = DriverManager.getConnection("jdbc:mysql://localhost:3306/products", "root", "abak");
            String sql = "select *from products where Category="+ "'" + com + "'"+ "group by Manufacturer" ;
            mystmt = myconn.createStatement();
            myrs = mystmt.executeQuery(sql);
            jComboBox2.removeAllItems();
            while (myrs.next()) {
                jComboBox2.addItem(myrs.getString("Manufacturer"));
            }

        } catch (Exception exc) {
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

